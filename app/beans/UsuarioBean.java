package beans;

import beans.search.BaseResultBean;

public class UsuarioBean extends BaseResultBean {
    public Long id;

    private String nome;

    public String email;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
}
